func TestClassifyApproveWhenScopeRespectedAndEvidenceAttached(t *testing.T) {
	task := Task{
		Task:     "bounded change",
		Allowed:  []string{"site/index.html", "site/style.css"},
		Changed:  []string{"site/index.html"},
		Blocked:  []string{"docs/research/**"},
		Evidence: []string{"manual sanity check"},
	}

	result := Classify(task)

	if result.Decision != "approve" {
		t.Fatalf("expected approve, got %q", result.Decision)
	}
	if result.ScopeStatus != "respected" {
		t.Fatalf("expected respected, got %q", result.ScopeStatus)
	}
	if len(result.OutOfScope) != 0 {
		t.Fatalf("expected no out-of-scope files, got %v", result.OutOfScope)
	}
}

func TestClassifyInspectWhenBlockedFileTouched(t *testing.T) {
	task := Task{
		Task:     "drifted change",
		Allowed:  []string{"site/index.html"},
		Changed:  []string{"site/index.html", "docs/research/notes.md"},
		Blocked:  []string{"docs/research/**"},
		Evidence: []string{"manual sanity check"},
	}

	result := Classify(task)

	if result.Decision != "inspect" {
		t.Fatalf("expected inspect, got %q", result.Decision)
	}
	if result.Reason != "blocked files were touched" {
		t.Fatalf("unexpected reason: %q", result.Reason)
	}
	if len(result.BlockedTouched) != 1 || result.BlockedTouched[0] != "docs/research/notes.md" {
		t.Fatalf("unexpected blocked touched set: %v", result.BlockedTouched)
	}
}

func TestRenderMarkdownIncludesDecisionAndEvidence(t *testing.T) {
	task := Task{
		Task:     "artifact drawer",
		Allowed:  []string{"site/index.html"},
		Changed:  []string{"site/index.html"},
		Blocked:  []string{"docs/research/**"},
		Evidence: []string{"drawer opens required artifacts"},
	}

	content := RenderMarkdown(task, Classify(task))

	expected := []string{
		"# Generated Review Artifact",
		"Decision: approve",
		"- `site/index.html`",
		"drawer opens required artifacts",
	}

	for _, fragment := range expected {
		if !strings.Contains(content, fragment) {
			t.Fatalf("expected rendered markdown to contain %q", fragment)
		}
	}
}

func TestGenerateFromFileUsesPlainTextManifestOverride(t *testing.T) {
	tempDir := t.TempDir()
	taskPath := filepath.Join(tempDir, "task.json")
	manifestPath := filepath.Join(tempDir, "changed.txt")

	taskJSON := `{
  "task": "bounded manifest change",
  "allowed": ["site/index.html"],
  "blocked": ["docs/research/**"],
  "changed": ["docs/research/notes.md"],
  "evidence": ["manual sanity check"]
}`
	if err := os.WriteFile(taskPath, []byte(taskJSON), 0o644); err != nil {
		t.Fatalf("write task: %v", err)
	}
	if err := os.WriteFile(manifestPath, []byte("# generated by runner\nsite/index.html\n"), 0o644); err != nil {
		t.Fatalf("write manifest: %v", err)
	}

	content, err := GenerateFromFile(taskPath, GenerateOptions{
		ChangedManifestPath: manifestPath,
	})
	if err != nil {
		t.Fatalf("generate from file: %v", err)
	}
	if !strings.Contains(content, "Decision: approve") {
		t.Fatalf("expected approve decision, got:\n%s", content)
	}
	if strings.Contains(content, "docs/research/notes.md") {
		t.Fatalf("expected manifest override to replace task changed files, got:\n%s", content)
	}
}

