func TestGenerateFromFileUsesGitRange(t *testing.T) {
	repoDir := t.TempDir()
	runGit(t, repoDir, "init")
	runGit(t, repoDir, "config", "user.name", "Specpunk Test")
	runGit(t, repoDir, "config", "user.email", "specpunk@example.com")

	writeFile(t, filepath.Join(repoDir, "site/index.html"), "before\n")
	writeFile(t, filepath.Join(repoDir, "site/style.css"), "before\n")
	runGit(t, repoDir, "add", ".")
	runGit(t, repoDir, "commit", "-m", "baseline")

	writeFile(t, filepath.Join(repoDir, "site/index.html"), "after\n")
	writeFile(t, filepath.Join(repoDir, "site/style.css"), "after\n")
	runGit(t, repoDir, "add", ".")
	runGit(t, repoDir, "commit", "-m", "bounded change")

	taskPath := filepath.Join(repoDir, "task.json")
	taskJSON := `{
  "task": "bounded git range change",
  "allowed": ["site/index.html", "site/style.css"],
  "blocked": ["docs/research/**"],
  "evidence": ["manual sanity check"]
}`
	writeFile(t, taskPath, taskJSON)

	content, err := GenerateFromFile(taskPath, GenerateOptions{
		ChangedGitRange: "HEAD~1..HEAD",
		WorkingDir:      repoDir,
	})
	if err != nil {
		t.Fatalf("generate from file: %v", err)
	}
	if !strings.Contains(content, "Decision: approve") {
		t.Fatalf("expected approve decision, got:\n%s", content)
	}
	if !strings.Contains(content, "- `site/index.html`") || !strings.Contains(content, "- `site/style.css`") {
		t.Fatalf("expected git-derived changed files, got:\n%s", content)
	}
}

